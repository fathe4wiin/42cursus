#include "libft.h"

int	ft_isalnum(char c)
{
	unsigned char	a;

	a = (unsigned)c;
	if (((a >= 'a' && a <= 'z') || (a >= 'A' && a <= 'Z')) || ((a >= '0'
				&& a <= '9')))
		return (1);
	else
		return (0);
}
