#include "libft.h"

int	ft_isalpha(char c)
{
	unsigned char	a;

	a = (unsigned)c;
	if ((a >= 'a' && a <= 'z') || (a >= 'A' && a <= 'Z'))
		return (1);
	else
		return (0);
}
